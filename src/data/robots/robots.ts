export function getRobotBySlug(slug: string): RobotData | undefined {
  return robots.find((r) => r.slug === slug);
}

export function getRobotsByCategory(categorySlug: string): RobotData[] {
  return robots.filter((r) => r.category_slug === categorySlug);
}

export function getFeaturedRobots(): RobotData[] {
  return [getRobotBySlug("dinerbot-t10")!, getRobotBySlug("kleenbot-c40")!, getRobotBySlug("butlerbot-w3")!].filter(Boolean);
}

export function getRelatedRobots(currentSlug: string, limit = 3): RobotData[] {
  const current = getRobotBySlug(currentSlug);
  if (!current) return [];
  return robots
    .filter((r) => r.category_slug === current.category_slug && r.slug !== currentSlug)
    .slice(0, limit);
}
